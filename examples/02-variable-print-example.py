# We can combine variable with print

kalimat = "Halo semua, saya suka nasi goreng"
print(kalimat)

# comma method
nama = "rusdi"
hobi = "nonton anime"
print("Halo, nama saya", nama, "dan hobi saya", hobi);

# f-string method
umur = 7
print(f"Umurku: {umur}")

# concatenation method
buah = "apel"
print("Saya membeli " + buah + " hari ini")

